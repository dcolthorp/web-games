import {
  type Axis,
  type BridgeFeature,
  type MapFeature,
  type MazeMap,
  type Point,
  type PortalFeature,
  type ResetFeature,
  type ReverseFeature,
  type SpinFeature,
  type SwitchFeature,
  type WellFeature,
  normalizeMazeMap,
} from "./model";

function spin(id: string, x: number, y: number, rotationQuarterDelta = 1): SpinFeature {
  return {
    kind: "spin",
    id,
    position: { x, y },
    props: { rotationQuarterDelta },
  };
}

function reset(id: string, x: number, y: number): ResetFeature {
  return {
    kind: "reset",
    id,
    position: { x, y },
    props: {},
  };
}

function reverse(id: string, x: number, y: number): ReverseFeature {
  return {
    kind: "reverse",
    id,
    position: { x, y },
    props: {},
  };
}

function bridge(id: string, x: number, y: number, underAxis: Axis): BridgeFeature {
  return {
    kind: "bridge",
    id,
    position: { x, y },
    props: { underAxis },
  };
}

function portal(
  id: string,
  x: number,
  y: number,
  targetPortalId: string
): PortalFeature {
  return {
    kind: "portal",
    id,
    position: { x, y },
    props: { targetPortalId },
  };
}

function well(id: string, x: number, y: number): WellFeature {
  return {
    kind: "well",
    id,
    position: { x, y },
    props: {},
  };
}

function mazeSwitch(
  id: string,
  x: number,
  y: number,
  targetWellIds: string[],
  timeoutMs = 10_000
): SwitchFeature {
  return {
    kind: "switch",
    id,
    position: { x, y },
    props: { timeoutMs, targetWellIds },
  };
}

function createMazeMap(
  id: number,
  name: string,
  terrain: string[],
  start: Point,
  exit: Point,
  features: MapFeature[]
): MazeMap {
  return {
    id,
    name,
    terrain,
    markers: { start, exit },
    features,
  };
}

